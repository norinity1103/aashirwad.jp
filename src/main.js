const defaultLanguage = "ja";
const languageStorageKey = "aashirwad-language";
const header = document.querySelector("[data-header]");
const toggle = document.querySelector(".nav-toggle");
const nav = document.querySelector(".site-nav");
const languageButtons = document.querySelectorAll("[data-lang-button]");
const descriptionMeta = document.querySelector('meta[name="description"]');
const ogTitleMeta = document.querySelector('meta[property="og:title"]');
const ogDescriptionMeta = document.querySelector('meta[property="og:description"]');

function getStoredLanguage() {
  try {
    const stored = window.localStorage.getItem(languageStorageKey);
    return stored === "en" ? "en" : defaultLanguage;
  } catch {
    return defaultLanguage;
  }
}

function storeLanguage(language) {
  try {
    window.localStorage.setItem(languageStorageKey, language);
  } catch {
    // Language switching still works without storage.
  }
}

function translateElement(element, language, useHtml) {
  const key = element.getAttribute(useHtml ? "data-i18n-html" : "data-i18n");
  if (!key) return;

  const value = translations[language][key] ?? translations[defaultLanguage][key];
  if (value === undefined) return;

  if (useHtml) {
    element.innerHTML = value;
  } else {
    element.textContent = value;
  }
}

function translateAttributes(element, language) {
  const config = element.getAttribute("data-i18n-attr");
  if (!config) return;

  for (const pair of config.split(";")) {
    const [attribute, key] = pair.split(":");
    if (!attribute || !key) continue;

    const value = translations[language][key] ?? translations[defaultLanguage][key];
    if (value !== undefined) element.setAttribute(attribute, value);
  }
}

function applyLanguage(language) {
  const normalizedLanguage = language === "en" ? "en" : defaultLanguage;

  document.documentElement.lang = normalizedLanguage;
  document.title = translations[normalizedLanguage]["meta.title"];
  descriptionMeta?.setAttribute("content", translations[normalizedLanguage]["meta.description"]);
  ogTitleMeta?.setAttribute("content", translations[normalizedLanguage]["meta.title"]);
  ogDescriptionMeta?.setAttribute("content", translations[normalizedLanguage]["meta.description"]);

  document.querySelectorAll("[data-i18n]").forEach((element) => translateElement(element, normalizedLanguage, false));
  document.querySelectorAll("[data-i18n-html]").forEach((element) => translateElement(element, normalizedLanguage, true));
  document.querySelectorAll("[data-i18n-attr]").forEach((element) => translateAttributes(element, normalizedLanguage));

  languageButtons.forEach((button) => {
    const isActive = button.getAttribute("data-lang-button") === normalizedLanguage;
    button.setAttribute("aria-pressed", String(isActive));
  });

  storeLanguage(normalizedLanguage);
}

toggle?.addEventListener("click", () => {
  const isOpen = toggle.getAttribute("aria-expanded") === "true";
  toggle.setAttribute("aria-expanded", String(!isOpen));
  header?.classList.toggle("is-open", !isOpen);
});

nav?.addEventListener("click", (event) => {
  if (event.target instanceof HTMLAnchorElement) {
    toggle?.setAttribute("aria-expanded", "false");
    header?.classList.remove("is-open");
  }
});

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.getAttribute("data-lang-button"));
  });
});

applyLanguage(getStoredLanguage());
